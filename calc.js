const add = (a, b) => {
    return a + b;
}
const subtract = (a, b) => {
    return a - b;
}
const multiply = (a, b) => {
    return a * b;
}
const division = (a, b) => {
    return a / b;
}

console.log(add(10, 5)); //15
console.log(subtract(10, 5)); //5
console.log(multiply(10, 5)); //50
console.log(division(10, 5)); //2